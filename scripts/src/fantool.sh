#!/usr/bin/env bash
set -euo pipefail

file=${1:?Need file name (mkv)}
[ -f "$file" ] || { echo "No such file: $file" >&2; exit 1; }

# Derivations
title="${file#* - }"
title="${title%.mkv}"

filename="${file%.*}"
extension="${file##*.}"

ass="${filename}.ass"
out="${filename}.new.mkv"

# echo "Title:     $title"
# echo "File name: $filename"
# echo "Extension: $extension"
# echo "ASS:       $ass"

